    private void uploadCarData(int idCar) {
        //search Car in bd
        car = control.getCar(idCar);
        //set that values on table
        txtModel.setText(car.getModel());
        txtBrand.setText(car.getBrand());
        txtColor.setText(car.getColor());
        txtEngine.setText(car.getEngine());
        txtPlate.setText(car.getLicencePlate());
        txtDoorsCount.setText(String.valueOf(car.getDoorCount()));
        
    }
