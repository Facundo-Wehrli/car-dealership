    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        // TODO add your handling code here:
        String model = txtModel.getText();
        String brand = txtBrand.getText();
        String engine = txtEngine.getText();
        String licencePlate = txtPlate.getText();
        String color = txtColor.getText();
        int doorCount = Integer.parseInt(txtDoorsCount.getText());
        control.editCar(car, model, brand, engine, licencePlate, color, doorCount);
        
        showMessage("Edited succesufully", "Info", "Car edited");
        //opens  bdtable screen
        ReadUpdateCar update = new ReadUpdateCar();
        update.setVisible(true);
        update.setLocationRelativeTo(null);
        
        this.dispose();
    }//GEN-LAST:event_btnEditActionPerformed
    public void showMessage(String mensaje, String tipo, String titulo) {
        JOptionPane optionPane = new JOptionPane(mensaje);
        if (tipo.equals("Info")) {
            optionPane.setMessageType(JOptionPane.INFORMATION_MESSAGE);
        } else if (tipo.equals("Error")) {
            optionPane.setMessageType(JOptionPane.ERROR_MESSAGE);
        }
        JDialog dialog = optionPane.createDialog(titulo);
        dialog.setAlwaysOnTop(true);
        dialog.setVisible(true);
        
    }

